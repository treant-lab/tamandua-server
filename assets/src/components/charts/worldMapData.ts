/**
 * Convert lat/lon coordinates to SVG path string using Mercator projection.
 * Must match the latLonToSvg function in the map component.
 */
export function coordinatesToSvgPath(
  coords: Coordinate[],
  mapWidth: number,
  mapHeight: number,
  mapPadding: number,
  mercatorScale: number = 0.5,
): string {
  if (coords.length < 3) return ''

  const toSvg = (lat: number, lon: number) => {
    const clampedLat = Math.max(-85, Math.min(85, lat))
    const x = ((lon + 180) / 360) * (mapWidth - 2 * mapPadding) + mapPadding
    const latRad = (clampedLat * Math.PI) / 180
    const mercN = Math.log(Math.tan(Math.PI / 4 + latRad / 2))
    const y = mapHeight / 2 - (mapWidth / (2 * Math.PI)) * mercN * mercatorScale

    return {
      x: Math.max(mapPadding, Math.min(mapWidth - mapPadding, x)),
      y: Math.max(mapPadding, Math.min(mapHeight - mapPadding, y)),
    }
  }

  const first = toSvg(coords[0][0], coords[0][1])
  let d = `M ${first.x.toFixed(1)},${first.y.toFixed(1)}`

  for (let i = 1; i < coords.length; i++) {
    const pt = toSvg(coords[i][0], coords[i][1])
    d += ` L ${pt.x.toFixed(1)},${pt.y.toFixed(1)}`
  }

  d += ' Z'
  return d
}

/**
 * Generate all SVG paths for the world map.
 * Results are cached based on dimensions.
 */
let cachedPaths: { key: string; paths: Array<{ name: string; d: string }> } | null = null

export function getWorldMapPaths(
  mapWidth: number,
  mapHeight: number,
  mapPadding: number,
  mercatorScale: number = 0.5,
): Array<{ name: string; d: string }> {
  const key = `${mapWidth}-${mapHeight}-${mapPadding}-${mercatorScale}`
  if (cachedPaths && cachedPaths.key === key) return cachedPaths.paths

  const paths = LANDMASSES.map(lm => ({
    name: lm.name,
    d: coordinatesToSvgPath(lm.coordinates, mapWidth, mapHeight, mapPadding, mercatorScale),
  }))

  cachedPaths = { key, paths }
  return paths
}
